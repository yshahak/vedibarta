	static String[] getPath(int position, int track) {
		String[] data = new String[2];
		String book;
		String par;
		track = track * 3;
		int pstn = position + 1;
		if (position < 12)
			book = "1-ber/";
		else if (position < 23) {
			book = "2-shm/";
			pstn = pstn - 12;
		} else if (position < 33) {
			book = "3-vay/";
			pstn = pstn - 23;
		} else if (position < 43) {
			book = "4-bam/";
			pstn = pstn - 33;
		} else {
			book = "5-dev/";
			pstn = pstn - 43;
		}
		if (pstn < 10)
			par = "0" + pstn + "-" + Names[position] + "/";
		else
			par = pstn + "-" + Names[position] + "/";
		String url = "http://www.vedibarta.org/Rashi_Tora_MP3/";
		data[0] = parashotNames[position][track]; // this the file name
		data[1] = url + book + par + data[0];
		return  data;
	}

